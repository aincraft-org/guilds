package org.aincraft.skilltree;

/**
 * Represents the skill tree branches available to guilds.
 * Each branch focuses on a different aspect of guild progression.
 */
public enum SkillBranch {
    /** Skills related to economic benefits and resource generation */
    ECONOMY,

    /** Skills related to territory expansion and chunk management */
    TERRITORY,

    /** Skills related to combat effectiveness and combat buffs */
    COMBAT
}
